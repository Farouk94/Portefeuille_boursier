    /**
     *
     * Updates jTable2, registers total.
     */
    private void UpdatejTable2() {
        DefaultTableModel modelPortfolio = new DefaultTableModel();
        total = new BigDecimal(0);
        modelPortfolio.setColumnIdentifiers(new String[]{"Name", "Symbol", "Stock Price (€)", "Bid Price", "Ask Price", "Quantity Bought", "Price Paid"});
        for (Company com : SQLConnectEmp.getPortfolioCompanies(userID)) {
            modelPortfolio.addRow(new String[]{com.getName(), com.getSymbol(), String.valueOf(com.getStockPrice().doubleValue()), String.valueOf(com.getBidPrice().doubleValue()), String.valueOf(com.getAskPrice().doubleValue()), Integer.toString(com.getNumberOwned()), String.valueOf((com.getStockPrice().multiply(new BigDecimal(Integer.toString(com.getNumberOwned()))).doubleValue()))});
            String disturbed = Integer.toString(com.getNumberOwned());
            BigDecimal aa = new BigDecimal(disturbed);
            total = total.add(com.getStockPrice().multiply(aa));
        }
        jLabel10.setText("Total is : " + String.valueOf(total.doubleValue()) + "€");
        jTable2.setModel(modelPortfolio);

    }

