    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        jTable2.clearSelection();
        jTextField1.setText(jList1.getSelectedValue().toString());

        String companyName = jTextField1.getText();
        jLabel15.setText("Selected company : '" + companyName + "'.   " + SQLConnectEmp.checkIfAutomated(userID, companyName));
        StringThirdParty = companyName;
        jPanel11.setVisible(false);
        if (evt.getClickCount() == 2 && jTabbedPane1.getSelectedIndex() == 0 || jTabbedPane1.getSelectedIndex() == 1) {
            jTabbedPane1.setSelectedIndex(1);
            jScrollPane5.setVisible(true);
            jLabel15.setText("Selected company : '" + companyName + "'.");
            try {
                if (companyName.isEmpty()) {
                    JOptionPane.showMessageDialog(rootPane, "Please provide a valid input.", "Bad input", WIDTH);
                } else {
                    SQLConnectEmp.refreshOneCompanyOnlyAtSearch(companyName);
                    Company com = SQLConnectEmp.lookForCompany(companyName, userID);
                    UpdatejTable3(com);
                    jButton6.setVisible(true);

                }
            } catch (Exception e) {
                System.err.println("Problem in button.\n" + e);
            }
            jLabel2.setVisible(false);
        }
        if (evt.getClickCount() == 1 && jTabbedPane1.getSelectedIndex() == 0) {
            jButton10.setVisible(false);
            jButton7.setVisible(false);
            jButton15.setVisible(false);
            jButton3.setVisible(false);
            jScrollPane2.setVisible(false);
            if (SQLConnectEmp.ifAutomated(userID, StringThirdParty) == 2) {
                jButton19.setText("De-Alert");
            } else {
                jButton19.setText("Alert");
            }

            jTextField6.setText(jList1.getSelectedValue().toString());

            String symbol = SQLConnectEmp.getSymbolOfCompanyFromDB(jList1.getSelectedValue().toString());
            if (SQLConnectMana.ifBanned(userID, symbol) == 1) {
                jButton13.setEnabled(false);
                jButton14.setEnabled(false);
                jTextField7.setEnabled(false);
            } else {
                jButton13.setEnabled(true);
                jButton14.setEnabled(true);
                jTextField7.setEnabled(true);
            }
        }


    }//GEN-LAST:event_jList1MouseClicked

