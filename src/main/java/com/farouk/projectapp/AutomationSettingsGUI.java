    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Please enter a valid input.", "Error", WIDTH);
        } else {
            try {
                String symb = SQLConnectEmp.getSymbolOfCompanyFromDB(sym);
                BigDecimal minPrice = new BigDecimal(jTextField1.getText());
                BigDecimal maxPrice = new BigDecimal(jTextField2.getText());
                int quantity = Integer.parseInt(jTextField3.getText());
                if ((minPrice.compareTo(BigDecimal.ZERO) == 1) && (maxPrice.compareTo(BigDecimal.ZERO) == 1) && (quantity > 0)) {
                    if (minPrice.compareTo(maxPrice) == 1) {
                        JOptionPane.showMessageDialog(rootPane, "Min < Max is to remember !! ", "Woow", WIDTH);
                    } else {

                        SQLConnectEmp.saveAutomateSettings(userID, symb, minPrice, maxPrice, quantity, 1, 0);
                        this.setVisible(false);
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Enter a positive number !!", "No !", WIDTH);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Please enter a number.", "No!", WIDTH);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

